<h1>About</h1>


<h3>The first idea</h3>
<p>
Pi is dedicated to Processing creatives. It aims to provide a comfortable space to create, exhibit and share interactive works, drawings and animations written in the Processing http://processing.org/ language (Java-like).</p>
<p>Pi uses the javascript port of Processing http://processingjs.org/ allowing users to code in javascript as well. 
Other libraries could be added in the future.</p>
<p>If you can embed a YouTube video or an swf Flash file in any web page, why shouldn't be as easy to embed a Processing sketch or any other canvas based application?

Note that the current site is an early Alpha with many known and unknown <a href="">bugs.
</p>

